package snake;

public class Apple {

	private int x, y;
	
	public Apple(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	/**
	 * Return X position of apple
	 */
	public int getX() {
		return x;
	}
	
	/**
	 * Return Y position of apple
	 */
	public int getY() {
		return y;
	}
	
}
